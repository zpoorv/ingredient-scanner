export function normalizeTranslationSource(source: string) {
  return source.replace(/\s+/g, ' ').trim();
}

export function translateAppText(
  languageCode: AppLanguageCode,
  source: string,
  values?: TranslationValues
) {
  const normalizedSource = normalizeTranslationSource(source);
  const translation = TRANSLATIONS[languageCode]?.[normalizedSource] ?? normalizedSource;

  if (!values) {
    return translation;
  }

  return Object.entries(values).reduce((result, [key, value]) => {
    return result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value ?? ''));
  }, translation);
}
